def _find_complex(keyword) -> str | None:
    """단지명 목록에서 키워드 매칭 (문자열 또는 튜플 지원)"""
    keywords = keyword if isinstance(keyword, tuple) else (keyword,)
    for cn in complex_names:
        if all(k in cn for k in keywords):
            return cn
    return None


